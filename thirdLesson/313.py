nums = list(map(int, input().split()))

def is_prime(n: int) -> bool:
    if n < 2:
        return False
    if n == 2:
        return True
    if n % 2 == 0:
        return False
    i = 3
    while i * i <= n:
        if n % i == 0:
            return False
        i += 2
    return True

primes = list(filter(lambda x: is_prime(x), nums))

if primes:
    print(*primes)
else:
    print("No primes")